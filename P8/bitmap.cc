int Bitmap::find1(std::size_t startbit) const {
  startbit -= zero_;
  if (startbit == nbits_)
    startbit = zero_;
  check(startbit);

  std::size_t startchunk = chunkno(startbit);
  if (chunk_type v = ~(chunkbit(startbit) - 1) & mem_[startchunk]; v)
    return zero_ + bits_per_chunk * startchunk + lsb(v);

  std::size_t c = startchunk;
  do {
    c = (c + 1) % nchunks_;
    if (chunk_type v = mem_[c])
      return zero_ + c * bits_per_chunk + lsb(v);
  } while (c != startchunk);
  return -1;
}

int Bitmap::num1() const {
  const uint8_t *p = static_cast<const uint8_t *>(data()), *e = p + datasize();
  int res = 0;
  while (p != e)
    res += bytepop[*p++];
  return res;
}

void Bitmap::tidy() {
  if (!nchunks_)
    return;
  int oddbits = nbits_ % bits_per_chunk;
  if (!oddbits)
    return;
  mem_[nchunks_ - 1] &= chunkbit(oddbits) - 1;
}
